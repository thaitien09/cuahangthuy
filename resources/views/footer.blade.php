<script>
document.addEventListener("DOMContentLoaded", function () {
    document.querySelectorAll(".filter-category").forEach(link => {
        link.addEventListener("click", function (event) {
            event.preventDefault();
            const categoryId = this.getAttribute("data-category");

            fetch(`/products/filter?category_id=${categoryId}`)
                .then(response => response.json())
                .then(products => {
                    if (!products.length) {
                        document.getElementById("product-list").innerHTML = `<p class="text-center text-muted">Không có sản phẩm nào</p>`;
                        return;
                    }

                    let html = `<div class="row">`;
                    products.forEach(product => {
                        html += `
                            <div class="col-md-3 col-sm-6 col-12 mb-3">
                                <div class="card">
                                    <img src="/images/${product.image}" class="card-img-top" alt="${product.name}">
                                    <div class="card-body p-2">
                                        <h5 class="card-title">${product.name}</h5>
                                        <p class="text-muted mb-1">Danh mục: ${product.category_name ?? 'Không xác định'}</p>
                                        <p class="text-muted mb-1">Loại: ${product.type_name ?? 'Không xác định'}</p>
                                        <p class="text-success fw-bold mb-2">
                                            ${new Intl.NumberFormat('vi-VN').format(product.sale_price)} VND
                                        </p>
                                        <a href="/products/${product.id}" class="btn btn-outline-dark btn-sm w-100">Xem chi tiết</a>
                                    </div>
                                </div>
                            </div>
                        `;
                    });

                    html += `</div>`; // Đóng `row`
                    
                    document.getElementById("product-list").innerHTML = html;
                    document.getElementById("product-list").scrollIntoView({ behavior: "smooth" });
                })
                .catch(error => console.error('Lỗi khi lấy dữ liệu:', error));
        });
    });
});

</script>

